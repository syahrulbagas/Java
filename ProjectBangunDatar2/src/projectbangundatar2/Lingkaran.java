public class Lingkaran {
    double hitungLuasL(int r) {
        return (r * r * 22 / 7);
    }
    
    double hitungLuasL(double r) {
        return (r * r * 22 / 7);
    }
    
    //hitung keliling
    double hitungKelilingL(int r) {
        return (2 * r * 22 / 7);
    }
    
    double hitungKelilingL(double r) {
        return (2 * r * 22 / 7);
    }
}
