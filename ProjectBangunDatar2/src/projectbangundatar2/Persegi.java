public class Persegi {
    int hitungLuasP(int sisi) {
        return (sisi * sisi);
    }
    
    double hitungLuasP(double sisi) {
        return (sisi * sisi);
    }
    
    //hitung keliling
    int hitungKelilingP(int sisi) {
        return (sisi * 4);
    }
    
    double hitungKelilingP(double sisi) {
        return (sisi * 4);
    }
}
